<template>
  <Form
    ref="loginForm"
    :model="form"
    :rules="rules"
    @keydown.enter.native="handleSubmit"
  >
    <FormItem prop="username">
      <Input
        v-model="form.username"
        placeholder="请输入工号"
      >
      <span slot="prepend">
        <Icon
          :size="16"
          type="ios-person"
        ></Icon>
      </span>
      </Input>
    </FormItem>
    <FormItem prop="password">
      <Input
        type="password"
        v-model="form.password"
        placeholder="请输入密码"
      >
      <span slot="prepend">
        <Icon
          :size="14"
          type="md-lock"
        ></Icon>
      </span>
      </Input>
    </FormItem>
    <FormItem>
      <Button
        @click="handleSubmit"
        type="primary"
        long
        :loading="isLoading"
      >登录</Button>
    </FormItem>
  </Form>
</template>
<script>
export default {
  name: "LoginForm",
  props: {
    usernameRules: {
      type: Array,
      default: () => {
        return [{ required: true, message: "工号不能为空", trigger: "blur" }];
      }
    },
    passwordRules: {
      type: Array,
      default: () => {
        return [{ required: true, message: "密码不能为空", trigger: "blur" }];
      }
    },
    isLoading: {
      type: Boolean,
      default: false
    }
  },
  data() {
    return {
      form: {
        username: "zhangsan",
        password: "123"
      }
    };
  },
  computed: {
    rules() {
      return {
        username: this.usernameRules,
        password: this.passwordRules
      };
    }
  },
  methods: {
    handleSubmit() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          this.$emit("on-success-valid", {
            username: this.form.username,
            password: this.form.password
          });
        }
      });
    }
  }
};
</script>
